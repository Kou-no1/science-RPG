const fs = require("fs");
const path = require("path");
const vm = require("vm");

const root = path.resolve(__dirname, "..");
const qDir = path.join(root, "data", "questions");

function loadExisting(file) {
  const sandbox = { window: { QUESTION_BANK: {} } };
  const code = fs.readFileSync(path.join(qDir, file), "utf8");
  vm.runInNewContext(code, sandbox, { filename: file });
  return sandbox.window.QUESTION_BANK;
}

const existing = {};
[
  "g5_u01.js",
  "g5_u02.js",
  "g5_u03.js",
  "g5_u07_solution.js"
].forEach((file) => Object.assign(existing, loadExisting(file)));

function q(id, tier, type, skill, stem, choices, answer, explanation) {
  return { id, tier, type, skill, stem, choices, answer, explanation };
}

function numbered(prefix, tier, code, rows, start = 1) {
  return rows.map((row, index) => q(
    `${prefix}-${code}-${String(start + index).padStart(3, "0")}`,
    tier,
    row[0],
    row[1],
    row[2],
    row[3],
    row[4],
    row[5]
  ));
}

function cloneBank(unitId) {
  const bank = existing[unitId] || { basic: [], boss: [], bonus: [] };
  return {
    basic: (bank.basic || []).slice(),
    boss: (bank.boss || []).slice(),
    bonus: (bank.bonus || []).slice()
  };
}

function appendTo(bank, tier, items, target) {
  const ids = new Set(bank[tier].map((item) => item.id));
  items.forEach((item) => {
    if (bank[tier].length >= target) return;
    if (!ids.has(item.id)) {
      bank[tier].push(item);
      ids.add(item.id);
    }
  });
}

function writeBank(file, unitId, bank) {
  const body = `/* =====================================================================\n` +
    ` * リカ・クエスト 問題バンク : ${unitId}\n` +
    ` * 生成元: tools/generate_g5_question_banks.js\n` +
    ` * basic/boss は5年単元スコープ内、bonus は🎓中学チャレンジ/💡トリビア。\n` +
    ` * ===================================================================== */\n` +
    `window.QUESTION_BANK = window.QUESTION_BANK || {};\n\n` +
    `window.QUESTION_BANK[${JSON.stringify(unitId)}] = ${JSON.stringify(bank, null, 2)};\n`;
  fs.writeFileSync(path.join(qDir, file), body, "utf8");
}

