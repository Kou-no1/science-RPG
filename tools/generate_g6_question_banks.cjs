const fs = require("fs");
const path = require("path");

const qDir = path.join(__dirname, "..", "data", "questions");
const ox = ["○", "×"];

function q(type, skill, stem, choices, answer, explanation) {
  return { type, skill, stem, choices, answer, explanation };
}

function mc(skill, stem, correct, wrongsOrFirst, ...rest) {
  if (Array.isArray(wrongsOrFirst)) {
    return q("mc4", skill, stem, [correct].concat(wrongsOrFirst), 0, rest[0]);
  }
  return q("mc4", skill, stem, [correct, wrongsOrFirst, rest[0], rest[1]], 0, rest[2]);
}

function tf(skill, stem, ok, explanation) {
  return q("ox", skill, stem, ox, ok ? 0 : 1, explanation);
}

function withIds(unitId, tier, rows) {
  const short = unitId.replace("_", "");
  const code = tier === "basic" ? "b" : tier;
  return rows.map((row, index) => Object.assign({
    id: `${short}-${code}-${String(index + 1).padStart(3, "0")}`,
    tier
  }, row));
}

function write(unitId, file, title, rows) {
  const bank = {
    basic: withIds(unitId, "basic", rows.basic),
    boss: withIds(unitId, "boss", rows.boss),
    bonus: withIds(unitId, "bonus", rows.bonus)
  };
  validate(unitId, bank);
  const text = `/* =====================================================================\n` +
    ` * リカ・クエスト 問題バンク : 6年「${title}」(${unitId})\n` +
    ` * basic/boss は単元スコープ内、bonus は🎓中学チャレンジ/💡トリビア。\n` +
    ` * ===================================================================== */\n` +
    `window.QUESTION_BANK = window.QUESTION_BANK || {};\n\n` +
    `window.QUESTION_BANK[${JSON.stringify(unitId)}] = ${JSON.stringify(bank, null, 2)};\n`;
  fs.writeFileSync(path.join(qDir, file), text, "utf8");
  console.log(`${unitId}: ${bank.basic.length}/${bank.boss.length}/${bank.bonus.length} -> ${file}`);
}

function validate(unitId, bank) {
  if (bank.basic.length !== 20 || bank.boss.length !== 10 || bank.bonus.length !== 15) {
    throw new Error(`${unitId} count mismatch`);
  }
  ["basic", "boss"].forEach((tier) => {
    bank[tier].forEach((item) => {
      if (/[🎓💡]/u.test(item.stem + item.explanation)) {
        throw new Error(`${item.id} has bonus label in ${tier}`);
      }
    });
  });
  bank.bonus.forEach((item) => {
    if (!/^[🎓💡]/u.test(item.stem)) throw new Error(`${item.id} missing bonus label`);
  });
}

